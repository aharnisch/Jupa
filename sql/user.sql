DROP TABLE user;

CREATE TABLE user (
  id       INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  username VARCHAR(255) NOT NULL,
  password VARCHAR(255) NOT NULL,
  forename VARCHAR(255),
  surname  VARCHAR(255)
);

CREATE UNIQUE INDEX user_username_idx
  ON user (username);

